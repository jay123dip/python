
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

#Additon of two number 
sum = num1 + num2
print('sum of {} and {} is {}'.format(num1,num2,sum))

# Substraction of two number
sub = num1 - num2
print('substraction of {} and {} is {}'.format(num1,num2,sub))

# Multiplication two number 
mul = num1 * num2
print('Multiplication of {} and {} is {}'.format(num1,num2,mul))

#Division of two number
div = num1 / num2
print('Division of {} and {} is {}'.format(num1,num2,div))

#Modulus operation
mod = num1 % num2
print('Mod of {} and {} is {}'.format(num1,num2,mod))